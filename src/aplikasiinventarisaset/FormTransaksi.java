    public void tampilan(){
        Object [] baris = {"No", "ID Peminjaman", "Aset", "Peminjam", "Tanggal Pinjam", "Tanggal Kembali"};
        tabMode = new DefaultTableModel(null, baris);
        tableTransaksi.setModel(tabMode);
        try {
                //String sql = "SELECT tb_peminjaman.id_peminjaman, tb_peminjaman.id_aset, tb_peminjaman.id_pengguna, tb_peminjaman.tgl_pinjam, tb_peminjaman.tgl_kembali, tb_aset.id_aset, tb_pengguna.id_pengguna FROM tb_peminjaman, tb_aset, tb_pengguna WHERE tb_peminjaman.id_aset = tb_aset.id_aset AND tb_peminjaman.id_pengguna = tb_pengguna.id_pengguna;";
                String sql = "Select * From tb_peminjaman";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String id = rs.getString("id_peminjaman");
                        String nk = rs.getString("id_aset");
                        String tk = rs.getString("id_pengguna");
                        String dp = rs.getString("tgl_pinjam");
                        String tp = rs.getString("tgl_kembali");

                        Object [] data = {no,id,nk,tk, dp, tp};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    } 
