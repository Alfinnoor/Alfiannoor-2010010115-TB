    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String  date = "yyyy-MM-dd";
        SimpleDateFormat dt = new SimpleDateFormat(date);
        String id_pengguna = cmbPeminjam.getSelectedItem().toString();
        String id_aset = cmbAset.getSelectedItem().toString();
        String tgl_pinjam = String.valueOf(dt.format(tglPinjam.getDate()));
        String tgl_kembali = String.valueOf(dt.format(tglKembali.getDate()));
        
        if (txtID.getText().equals("")|| tglPinjam.getDate().equals("") || tglKembali.getDate().equals("") ){
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!!");
        } else {
            try {
               int s;
               String sql = "INSERT INTO tb_peminjaman VALUES ('"+txtID.getText()+"', '"+id_aset+"', '"+id_pengguna+"'  , '"+tgl_pinjam+"'  , '"+tgl_kembali+"')";
               st = conn.createStatement();
               s = st.executeUpdate(sql);
               if (s == 1){
                JOptionPane.showMessageDialog(null, "Berhasil disimpan");
                clear();
                tampilan();
               }
           } catch (Exception e){
                System.out.println(e.toString());
           }    
        }   
    }//GEN-LAST:event_btnSimpanActionPerformed

