    public void tampilan(){
        Object [] baris = {"No", "ID Aset", "Nama Aset", "Jenis", "Lokasi"};
        tabMode = new DefaultTableModel(null, baris);
        tableAset.setModel(tabMode);
        try {
                String sql = "SELECT * FROM tb_Aset";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String id = rs.getString("id_aset");
                        String nk = rs.getString("nama_aset");
                        String tk = rs.getString("jenis_aset");
                        String dp = rs.getString("lokasi");

                        Object [] data = {no,id,nk,tk, dp};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
    
