    public void tampilan(){
        Object [] baris = {"No", "ID Pengguna", "Nama Pengguna", "Jabatan", "Departemen"};
        tabMode = new DefaultTableModel(null, baris);
        tablePengguna.setModel(tabMode);
        try {
                String sql = "SELECT * FROM tb_pengguna";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String id = rs.getString("id_pengguna");
                        String nk = rs.getString("nama_pengguna");
                        String tk = rs.getString("jabatan");
                        String dp = rs.getString("departemen");

                        Object [] data = {no,id,nk,tk, dp};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
    
